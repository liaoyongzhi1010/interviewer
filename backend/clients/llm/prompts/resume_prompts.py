"""
简历解析提示词模块
"""


def get_resume_extraction_prompt(markdown_content: str) -> str:
    """
    从Markdown格式的简历中提取结构化数据的提示词

    Args:
        markdown_content: OCR解析后的Markdown格式简历内容

    Returns:
        LLM提示词
    """
    prompt = f"""你是一位资深技术面试官助理。请从下面的 OCR Markdown 简历中，提取“用于面试出题”的结构化信息。

简历内容：
{markdown_content}

任务目标：输出高质量、可直接用于技术面试的问题生成数据。

请提取并返回以下字段：
1. name：候选人姓名（无法确定则为空字符串）
2. position：目标岗位或最匹配岗位（例如：后端开发工程师、Java开发工程师、算法工程师等）
3. skills：技能关键词数组（用于面试题标签）
4. projects：项目经验数组（每个元素是一段可直接用于追问的问题素材）

提取规则（非常重要）：
1. OCR 容错：
- 自动忽略邮箱/电话/地址等噪声对字段的干扰。
- 处理中英文标点混杂、错别字、重复词、换行断裂问题。

2. position 推断：
- 若简历未明确“应聘岗位”，根据“技术栈+项目经历+实习内容”推断一个最匹配岗位。
- 不要输出泛化岗位（如“工程师”）；要具体到技术方向（如“Java后端开发工程师”）。

3. skills 规范化：
- 仅保留技术技能，不要放软技能（如“沟通能力强”）。
- 合并同义词并标准化命名（如 Spring Boot/SpringBoot 统一为 Spring Boot，Mybatis 统一为 MyBatis）。
- 优先保留对面试最有价值的技术关键词（语言、框架、中间件、数据库、并发、分布式、AI/LLM 相关）。
- 去重后输出，按“基础语言 -> 框架 -> 数据库/中间件 -> 工程/架构 -> AI能力”排序。

4. projects 面试化改写：
- 将“项目经历”和“实习经历”都可纳入 projects。
- 每个项目条目建议格式：
  "项目名 | 时间 | 技术栈: ... | 个人职责: ... | 难点/优化: ... | 结果: ..."
- 尽量提取可追问的技术细节：并发、一致性、性能优化、架构设计、故障处理、模型应用方式等。
- 若简历中有可量化结果（如提升xx%、QPS、时延下降等）要保留在“结果”中。
- 保持每条 60~220 字，信息完整但不过长。

返回约束：
- 必须返回合法 JSON 对象，不要输出任何额外文字。
- 不要使用 markdown 代码块。
- skills 和 projects 必须是数组。
- 若字段无法提取，返回空字符串或空数组。

JSON 格式严格如下：
{{
  "name": "候选人姓名",
  "position": "目标岗位",
  "skills": [
    "Java",
    "Spring Boot",
    "Redis"
  ],
  "projects": [
    "短链接营销推广平台 | 2023.10-2024.07 | 技术栈: Spring Boot, Redis, RabbitMQ, Flink, Kafka | 个人职责: 负责账号与商品服务、短链生成与扣减流量包链路 | 难点/优化: 通过MQ延迟队列+Task表保障分布式事务一致性 | 结果: 提升高并发场景稳定性并支持实时数据分析",
    "掌上同城酒库配送平台 | 2023.11-2024.04 | 技术栈: Spring Boot, Redis, MyBatis, WebSocket | 个人职责: 负责订单管理、秒杀链路、AI客服对接 | 难点/优化: Redis预减库存+MQ异步下单+限流策略缓解峰值流量 | 结果: 提升系统抗压能力并降低超卖风险"
  ]
}}"""

    return prompt
